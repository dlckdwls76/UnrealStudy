#include "Study04CDO.h"

AStudy04CDO::AStudy04CDO()
{
    PrimaryActorTick.bCanEverTick = false;
    
    // 생성자에서 설정한 값은 CDO에 직접 기록됩니다. 
    // 즉, 앞으로 태어날 모든 AStudy04CDO 인스턴스들의 "초기 기본값"이 100으로 고정됩니다.
    BaseDamage = 100;
}

void AStudy04CDO::BeginPlay()
{
    Super::BeginPlay();
    
    // BeginPlay는 게임이 시작되고 해당 "인스턴스"가 레벨에 스폰되었을 때 호출됩니다.
    // 따라서 여기서 값을 변경하면, 이 값은 CDO(원본)에는 전혀 영향을 주지 않고 현재 이 개체에만 적용됩니다.
    BaseDamage = 200;

    // ==============================================================================
    // [예시 문제: CDO 문제]
    // Q: 한 개발자가 무기의 기본 데미지를 게임 실행 중에 200으로 영구적으로 올리고 싶어서,
    //    AWeapon::StaticClass()->GetDefaultObject<AWeapon>() 함수를 이용해 CDO를 직접 가져온 뒤, 
    //    CDO의 BaseDamage를 200으로 수정해버렸습니다. 이후 새로운 무기를 스폰하면 데미지는 얼마가 되나요?
    // 
    // A: 200이 됩니다.
    //    이유: CDO는 스폰되는 객체들의 "거푸집(템플릿)" 역할을 합니다. 거푸집 자체의 값을 200으로 
    //         변형해 버렸으므로, 이후 이 거푸집을 통해 스폰되는 모든 무기 인스턴스는 200의 데미지를 
    //         기본값으로 달고 태어나게 됩니다. (주의: 일반적인 상황에서는 권장하지 않는 편법입니다!)
    // ==============================================================================
}
