    //    이 적이 내 시야의 "앞쪽(전방)"에 있는지 "뒤쪽(후방)"에 있는지 파악하려고 합니다. 
    //    나의 정면 벡터(ForwardVector)와 적을 향하는 방향 벡터(DirectionVector)를 내적(Dot)했을 때, 
    //    결과값이 어떤 범위에 있어야 적이 내 '앞쪽'에 있다고 판단할 수 있을까요?
    // 
    // A: 결과값이 0보다 커야 합니다. (DotResult > 0)
    //    이유: 두 정규화된 벡터의 내적 결과는 코사인(Cos) 각도를 의미합니다. 
